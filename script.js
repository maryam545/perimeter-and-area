// start rectangle
let width = 6;
let height = 10;
const rectangleArea = width * height;
console.log(`rectangleArea = ` + rectangleArea);
const rectanglePerimeter = width * 2 + height * 2;
console.log(`rectanglePerimeter = ` + rectanglePerimeter);
// end rectangle
// start circle
let pi = 3.14;
let radius = 2;
const circleArea = pi * radius * 2;
console.log(`circleArea = ` + circleArea);
const circlePerimeter = radius * radius * pi;
console.log(`circlePerimeter = ` + circlePerimeter);

// end circle
// start trapezoid
let smallBase = 2;
let bigBase = 4;
let heightTrapezoid = 6;
const trapezoidArea = 2 * smallBase + 2 * bigBase;
console.log(`trapezoidArea = ` + trapezoidArea);
const trapezoidPerimeter = ((smallBase + bigBase) * heightTrapezoid) / 2;
console.log(`trapezoidPerimeter = ` + trapezoidPerimeter);
// end trapezoid
